    private void mujButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mujButonActionPerformed
        Double vysledek = 0.0;
        Double cislo1 = Double.parseDouble(jTextField1.getText());
        Double cislo2 = Double.parseDouble(jTextField2.getText());
        String operace = (String) jComboBox1.getSelectedItem();
        
        switch (operace) {
            case "+":
                vysledek = cislo1+cislo2;
                break;
            case "-":
                vysledek = cislo1-cislo2;               
                break;
            case "*":
                vysledek = cislo1*cislo2;
                break;
            case "/":
                if(cislo2==0) {
                    JOptionPane.showMessageDialog(null, "Nulou nelze dělit!");
                }
                else {
                vysledek = cislo1/cislo2;
                }
                break;
        }
        jTextField3.setText(String.format("%.2f", vysledek));
      
       
     
       
    }//GEN-LAST:event_mujButonActionPerformed

